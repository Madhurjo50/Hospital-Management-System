    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String username = jTextField2.getText().trim();
    String password = String.valueOf(jPasswordField1.getPassword());
    String role = jComboBox1.getSelectedItem().toString();

    // Empty check
    if(username.isEmpty() || password.isEmpty()){
        javax.swing.JOptionPane.showMessageDialog(null, "Please enter username & password!");
        return;
    }

    // Connect to database
    Connection conn = connectDB();
    if(conn == null){
        javax.swing.JOptionPane.showMessageDialog(null, "Database connection failed!");
        return;
    }

    try {
        String sql = "SELECT * FROM user WHERE username=? AND password=? AND role=?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, username);
        pst.setString(2, password);
        pst.setString(3, role);

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {

    // ðŸ”´ Doctor approval check
    if (role.equals("Doctor")) {
        String status = rs.getString("status");

        if (!status.equalsIgnoreCase("approved")) {
            JOptionPane.showMessageDialog(
                null,
                "Your account is pending admin approval!"
            );
            conn.close();
            return;
        }
    }

    JOptionPane.showMessageDialog(null, "Login Successful as " + role);

    switch (role) {
        case "Admin":
            new AdminDashboard().setVisible(true);
            break;

        case "Doctor":
            new DoctorDashboard().setVisible(true);
            break;

        case "Staff":
            new StaffDashboard().setVisible(true);
            break;

        case "Patient":
            new PatientDashboard().setVisible(true);
            break;
    }

    this.dispose();

} else {
    JOptionPane.showMessageDialog(
        null,
        "Invalid Credentials or Wrong Role Selected!"
    );
}


        conn.close();

    } catch(Exception e){
        javax.swing.JOptionPane.showMessageDialog(null, e);
    }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

